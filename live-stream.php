// Countdown for stream launch
function updateCountdown() {
  const target = new Date('2025-08-12T16:24:00').getTime();
  const now = new Date().getTime();
  const diff = target - now;
  if (diff < 0) {
    document.getElementById('countdown').innerHTML = 'The stream is live!';
    return;
  }
  const days = Math.floor(diff / (1000 * 60 * 60 * 24));
  const hours = Math.floor((diff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
  const minutes = Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60));
  const seconds = Math.floor((diff % (1000 * 60)) / 1000);
  document.getElementById('countdown').innerHTML = `
    <div class='flex flex-wrap justify-center gap-2 sm:gap-4'>
      <div class='flex flex-col items-center min-w-[56px] sm:min-w-[70px]'>
        <div class='bg-red-700 text-white text-2xl sm:text-4xl md:text-5xl font-bold px-3 sm:px-6 py-2 rounded-md shadow w-full text-center'>${String(days).padStart(2, '0')}</div>
        <span class='text-xs sm:text-base text-white bg-black bg-opacity-60 rounded px-1 sm:px-2 mt-2 text-center w-full capitalize tracking-wide'>Days</span>
      </div>
      <div class='flex flex-col items-center min-w-[56px] sm:min-w-[70px]'>
        <div class='bg-red-700 text-white text-2xl sm:text-4xl md:text-5xl font-bold px-3 sm:px-6 py-2 rounded-md shadow w-full text-center'>${String(hours).padStart(2, '0')}</div>
        <span class='text-xs sm:text-base text-white bg-black bg-opacity-60 rounded px-1 sm:px-2 mt-2 text-center w-full capitalize tracking-wide'>Hours</span>
      </div>
      <div class='flex flex-col items-center min-w-[56px] sm:min-w-[70px]'>
        <div class='bg-red-700 text-white text-2xl sm:text-4xl md:text-5xl font-bold px-3 sm:px-6 py-2 rounded-md shadow w-full text-center'>${String(minutes).padStart(2, '0')}</div>
        <span class='text-xs sm:text-base text-white bg-black bg-opacity-60 rounded px-1 sm:px-2 mt-2 text-center w-full capitalize tracking-wide'>Minutes</span>
      </div>
      <div class='flex flex-col items-center min-w-[56px] sm:min-w-[70px]'>
        <div class='bg-red-700 text-white text-2xl sm:text-4xl md:text-5xl font-bold px-3 sm:px-6 py-2 rounded-md shadow w-full text-center'>${String(seconds).padStart(2, '0')}</div>
        <span class='text-xs sm:text-base text-white bg-black bg-opacity-60 rounded px-1 sm:px-2 mt-2 text-center w-full capitalize tracking-wide'>Seconds</span>
      </div>
    </div>
  `;
}
setInterval(updateCountdown, 1000);
updateCountdown();
</script> 